        public static RSAParameters RSA_BeToLeKey(byte[] data)
        {
            var rsa = new RSAParameters();
            rsa.Exponent = data.Substring(4, 4);
            rsa.Modulus = Swap8(data.Substring(0x10, 0x100));

            return rsa;
        }

        static byte[] Swap8(byte[] input)
        {
            byte[] buffer = new byte[input.Length];
            Array.Copy(input, buffer, input.Length);
            for (int x = 0; x < input.Length; x += 8)
                Array.Reverse(buffer, x, 8);

            return buffer;
        }

